package interface_adapter;

public class DashboardState {
    // Example fields, adjust according to your actual needs
    private String selectedSection = "";
    private String errorMessage = null;
    private boolean isDataLoading = false;

    // Copy constructor
    public DashboardState(DashboardState copy) {
        selectedSection = copy.selectedSection;
        errorMessage = copy.errorMessage;
        isDataLoading = copy.isDataLoading;
    }

    // Default constructor
    public DashboardState() {}

    // Getters and Setters
    public String getSelectedSection() {
        return selectedSection;
    }

    public void setSelectedSection(String selectedSection) {
        this.selectedSection = selectedSection;
    }

    public String getErrorMessage() {
        return errorMessage;
    }

    public void setErrorMessage(String errorMessage) {
        this.errorMessage = errorMessage;
    }

    public boolean isDataLoading() {
        return isDataLoading;
    }

    public void setDataLoading(boolean dataLoading) {
        isDataLoading = dataLoading;
    }

    // Additional methods related to dashboard state can be added here
}
